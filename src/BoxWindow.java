public class BoxWindow {
	
	public static void main(String[] args) {
		System.out.print("+===+===+");
		for(int i = 0; i<2 ; i++){			
			System.out.println();
			for(int j = 0 ; j <3 ; j++){				
				System.out.print("|");
				System.out.print("   |   ");
				System.out.print("|");
				System.out.println();				
			}
			System.out.print("+===+===+");
		}
		
	}

}
